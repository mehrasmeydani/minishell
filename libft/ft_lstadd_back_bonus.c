#include "../header/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (lst)
	{
		tmp = ft_lstlast(*lst);
		if (tmp)
			(tmp)->next = new;
		else
			*lst = new;
	}
}
