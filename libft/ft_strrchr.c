#include "../header/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*tmp;

	i = 0;
	while (i <= ft_strlen(s))
	{
		if (s[ft_strlen(s) - i] == (char)c)
			return (tmp = (char *)(s + ft_strlen(s) - i), tmp);
		i++;
	}
	return (NULL);
}
