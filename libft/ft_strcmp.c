#include "../header/libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	size_t	i;
	t_uc	*s_1;
	t_uc	*s_2;

	i = 0;
	s_1 = (t_uc *)s1;
	s_2 = (t_uc *)s2;
	while (s_1[i] == s_2[i] && s_1[i] != '\0' && s_2[i] != '\0')
		i++;
	return (s_1[i] - s_2[i]);
}
